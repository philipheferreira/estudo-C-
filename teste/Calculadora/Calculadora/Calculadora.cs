using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Classe{
    class Calculadora{


        public double valor1;
        public double valor2;

        public double soma()
        {
            return valor1 + valor2;
        }

        public double sub()
        {
            return valor1 - valor2;
        }

        public double mult()
        {
            return valor1 * valor2;
        }

        public double div()
        {
            return valor1 / valor2;
        }

    }
}
